#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*tmp;

	tmp = (char *)malloc((ft_strlen(s) + 1) * sizeof(*s));
	if (!tmp)
		return (NULL);
	i = 0;
	while (s[i])
	{
		tmp[i] = s[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
