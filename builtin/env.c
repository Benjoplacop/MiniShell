#include "../minishell.h"

void	env(char **envp)
{
	int	i;

	i = 0;
	while (envp[i])
	{
		if (i != 0)
			write(1, "\n", 1);
		ft_putstr_fd(envp[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
